               <p class="pad10_10">（二）名词解释：</p>
               <p class="pad10_10">1、未结算：可结算，未到结算期。</p>
               <p class="pad10_10">2、待结算：可结算，已到结算期，等待“爱心购”财务处理。</p>
               <p class="pad10_10">3、结算中：“爱心购”财务正在进行汇款结算流程。</p>
               <p class="pad10_10">4、结算完成：“爱心购”财务已将款项汇入商家帐户。</p>
               <p class="pad10_10">（三）可进入结算流程的订单：线上缴款订单、自发COD订单，按发货5天进入结款账期；代发COD订单，按快递系统已送达3天进入结款账期;</p>
               <p class="pad10_10">（四）如商户发生任何违反本规定的违约行为，“爱心购”有权单方暂停、终止或扣减网上货款结算，由此造成的损失完全由商户承担。</p>
               <p class="pad10_10 fontwB">第四十九条 商户评估淘汰标准</p>
               <p class="pad10_10">“爱心购”按照季度、年度根据商户评估淘汰标准对商户进行审核评估，对于未达到评估标准的商户，“爱心购”有权终止合作协议。</p>
               <p class="pad10_10">(一)评估准则：以每年年初公布为准</p>
               <p class="pad10_10">（二）评估实施：入驻时间不满三个月的商户不参与评估；入驻时间不满十二个月的商户不参与年度评估。</p>
               <p class="pad10_10">（三）评估执行：及格商户，“爱心购”可以提出建议，商户应积极配合进一步提高服务。未及格商户，“爱心购”有权终止合作协议。</p>
               <p class="pad10_10">第九章 禁止性规定</p>
               <p class="pad10_10 fontwB">第五十条 违背承诺</p>
               <p class="pad10_10">（一）定义：</p>
               <p class="pad10_10">违背承诺，是指商户未按照承诺向买家提供以下服务，妨害买家权益的行为，具体行为如下:</p>
               <p class="pad10_10">1、“爱心购”判定商户确实应该承担退货退款等售后保障责任但商户拒绝承担的；</p>
               <p class="pad10_10">2、“爱心购”判定商户确实应该承担七天无理由退换货、退一赔三、数码维修等售后保障责任但商户拒绝承担的；</p>
               <p class="pad10_10">3、商户拒绝提供或者拒绝按照承诺的方式提供发票的；</p>
               <p class="pad10_10">4、加入货到付款等服务的商家，但拒绝提供上述服务的；</p>
               <p class="pad10_10">5、加入特殊优惠活动的商家，未按照活动要求提供服务的；</p>
               <p class="pad10_10">6、拒绝按照买家购买的价格成交或者拒绝提供承诺服务的；</p>
               <p class="pad10_10">7、商户拒绝按照承诺给““爱心购””、买家提供服务的;</p>
               <p class="pad10_10">8、商户未履行其它承诺的;</p>
               <p class="pad10_10">（二）违约：</p>
               <p class="pad10_10">1、公告警示;</p>
               <p class="pad10_10">2、商户违背以上承诺的，每次扣3分;</p>
               <p class="pad10_10 fontwB">第五十一条 线下交易</p>
               <p class="pad10_10">（一）定义：</p>
               <p class="pad10_10">线下交易是指通过“爱心购”购物平台下订单，不通过“爱心购”支付系统付款的订单，即由买家直接联系商户进行双方协商购买的交易行为。</p>
               <p class="pad10_10">（二）认定：</p>
               <p class="pad10_10">“爱心购”对通过“爱心购”购物平台产生的订单实时监控，根据订单实际发生与否确定线下交易行为是否成立。</p>
               <p class="pad10_10">（三）违约：</p>
               <p class="pad10_10">商家违背以上承诺的，每次扣18分（签保底协议的除外）;</p>
               <p class="pad10_10">赔偿：商户需向“爱心购”缴纳5倍的线下交易订单佣金，作为赔偿;</p>
               <p class="pad10_10 fontwB">第五十二条 虚假宣传</p>
               <p class="pad10_10">是指商业活动中商家利用广告或其他宣传方法对商品或者服务发布与实际内容不相符的虚假信息，导致买家产生误解的行为；虚假宣传的，“爱心购”将对商家采取每次扣12分的处罚，对涉及虚假宣传的商品或信息进行删除，同时因虚假宣传违规损害买家体验的，商家需按退一赔三最低500元标准对买家予以赔付；</p>
               <p class="pad10_10 fontwB">第五十三条 恶意骚扰买家</p>
               <p class="pad10_10">指商家交易中或交易后采取恶劣手段骚扰买家，妨害他人合法权益的行为,每次扣12分。</p>
               <p class="pad10_10 fontwB">第五十四条 泄露他人信息</p>
               <p class="pad10_10">是指未经允许发布、传递他人隐私信息，涉嫌侵犯他人隐私权的行为。泄露他人信息的，每次扣6分。</p>
               <p class="pad10_10 fontwB">第五十五条 盗用他人账户:</p>
               <p class="pad10_10">指商家盗用他人“爱心购”账户，涉嫌侵犯他人相关信息、财产权的行为,每次扣48分。</p>
               <p class="pad10_10 fontwB">第五十六条 骗取他人财物</p>
               <p class="pad10_10">是指商家以非法获利为目的，非法获取他人财物，涉嫌侵犯他人财产权的行为,每次扣48分。</p>
               <p class="pad10_10">第十章 服务管理规定</p>
               <p class="pad10_10 fontwB">第五十七条 商家服务政策及规范</p>
               <p class="pad10_10">（一）定义和认定：</p>
               <p class="pad10_10">1、入驻商家需按下列规定执行。</p>
               <p class="pad10_10">（二）违约：</p>
               <p class="pad10_10">1、若商家服务态度恶劣、使用不文明语言，罚款50元~500元，关闭店铺7天，影响恶劣的追加500--2000元的罚款；</p>
               <p class="pad10_10">2、退换货退款售后类问题，如商家未按规定时间回复处理意见，则视为商家对客户提出的售后服务无异议，“爱心购”有权按照客户要求为其回复处理意见，商家应按照“爱心购”的处理意见为客户提供售后服务，因此而可能产生的损失或额外费用将由商家自行承担；</p>
               <p class="pad10_10">3、对于被媒体发布从而对“爱心购”产生不利社会影响的客户投诉或被工商局等相关部门受理的重大客户投诉，商家未按处理时效处理，“爱心购”有权要求卖家承担人民币10000元以上的违约金，并要求卖家立即下架相应的商品，停止销售，进行整改，直至终止与卖家的合作，如因此给“爱心购”造成损失的，卖家应承担全部责任；</p>
               <p class="pad10_10">4、对于通过论坛、微博等途径传播对“爱心购”产生不利社会影响的客户投诉或被工商局等相关部门受理的一般客户投诉，商家未按处理时效处理，“爱心购”有权要求卖家承担人民币5000元以上的违约金，并要求卖家立即下架相应的商品,如因此给“爱心购”造成损失的，卖家应承担全部责任。</p>
               <p class="pad10_10">5、问题响应时效 ：买家的诉求，“爱心购”会通知（通知形式包括但不限于工单、电话、邮件、““爱心购””在线客服）商家处理，商家需按以下时效进行处理。</p>
               <p class="pad10_10">（1）咨询类+查询类问题，商家需在接到客服反馈后3小时内，给出处理方案或者处理时效，处理时效应在24小时（以““爱心购””通知时间计算自然小时）内。</p>
               <p class="pad10_10">（2）退换货退款售后类问题，商家需在接到客服反馈后3小时内，给出处理方案或者处理时效，处理时效应在24小时（以““爱心购””通知时间计算自然小时）内。</p>
               <p class="pad10_10">（3）媒体、工商、消协投诉的，商家需在接到客服反馈后1小时内，给出处理方案或者处理时效，处理时效应在24小时（以““爱心购””通知时间计算自然小时）内。</p>
               <p class="pad10_10">（4）论坛、微博等公共平台，商家需在接到客服反馈后1小时内，给出处理方案或者处理时效，处理时效应在24小时（以““爱心购””通知时间计算自然小时）内。</p>
               <p class="pad10_10">6、违反本规定任何一项，处罚每次扣1分。</p>
               <p class="pad10_10 fontwB">第五十八条 不配合平台管理</p>
               <p class="pad10_10">1、是指商家在日常运营中不配合平台管理的行为。每次扣2分。包括但不限于骚扰、谩骂、恐吓平台工作人员，散播恶意攻击平台的消息等情形；对于不配合平台管理的商家““爱心购””将采取警告公示、商品下架、限制发布商品等违规处理措施。</p>
               <p class="pad10_10">2、“爱心购”有权对发货率、投诉率等指标达成较差商家进行约谈，需商家给出书面分析及改进措施。持续未改进将扣除0.5分，情节严重追加扣除0.5分。</p>
               <p class="pad10_10">第十一章 其他</p>
               <p class="pad10_10 fontwB">第五十九条 帐户密码</p>
               <p class="pad10_10">（一）商户凭“爱心购”商户名和密码进入“爱心购”商户后台-商家管理系统建立和维护自己的网上店铺。为确保系统安全，建议商户定期更改密码。</p>
               <p class="pad10_10">（二）商户对于“爱心购”商户名和密码应严格保密并不得泄露给任何无关的第三方，如因商户保管不善而造成任何损失，商户应承担全部责任。</p>
               <p class="pad10_10 fontwB">第六十条 税款缴纳</p>
               <p class="pad10_10">双方应各自按照有关法律法规和国家政策的规定向有关的税务机关纳税。</p>
               <p class="pad10_10 fontwB">第六十一条 保密条款</p>
               <p class="pad10_10">（一）任何一方对于因签署或履行本规定而了解或接触到的对方的机密资料和信息（以下简称"保密信息"）均应保密；非经对方书面同意，任何一方不得向第三方泄露、给予或转让该等保密信息。</p>
               <p class="pad10_10">（二） 如对方提出要求，任何一方均应将载有对方保密信息的任何载体，按对方要求归还对方，或予以销毁，或进行其他处置，并且不得继续使用这些保密信息。</p>
               <p class="pad10_10">（三）在本规定终止之后，各方在本规定项下的义务并不随之终止，各方仍需遵守本规定之保密条款，履行其所承诺的保密义务，直到其他方同意其解除此义务。</p>
               <p class="pad10_10">（四）违约</p>
               <p class="pad10_10">商家违背以上承诺的，每次扣36分。</p>
               <p class="pad10_10 fontwB">第六十二条 利益冲突</p>
               <p class="pad10_10">（一）定义：</p>
               <p class="pad10_10">1、在本规定有效期内，如有任何“爱心购”或“爱心购”关联企业在职员工成为商户的股东或者高级管理人员，商户必须立即书面通知“爱心购”，如商户故意隐瞒此情况，“爱心购”有权随时提前终止商户的业务而无需承担任何责任，由此造成的全部损失由商户自行承担。“爱心购”无需退还商户已向“爱心购”交纳的所有费用。</p>
               <p class="pad10_10">2、商户不得为牟取不正当利益而对任何“爱心购”或“爱心购”关联企业在职员工有任何贿赂以及恶意投诉恶意诋毁“爱心购”行象的行为，否则“爱心购”有权随时单方提前终止本规定而无需承担任何责任，同时，商户应赔偿“爱心购”因此遭受的全部损失。</p>
               <p class="pad10_10">（二）违约：</p>
               <p class="pad10_10">商家违背以上承诺的，每次扣36分。</p>
               <p class="pad10_10 fontwB">第六十三条 不可抗力</p>
               <p class="pad10_10">（一）"不可抗力"是指本规定双方不能合理控制、不可预见或即使预见亦无法避免的事件，该事件妨碍、影响或延误任何一方根据本规定履行其全部或部分义务。该事件包括但不限于政府行为、自然灾害、战争、黑客袭击或任何其它类似事件等。</p>
               <p class="pad10_10">（二）遭受不可抗力事件的一方可暂时中止履行本规定项下的义务直至不可抗力事件的影响消除为止，并且无需为此而承担违约责任。但应尽最大努力克服该事件，减轻其负面影响。</p>
               <p class="pad10_10">（三）如不可抗力事件影响任何一方履行本规定项下义务的时间持续超过20天或累计超过30天，双方应就是否继续履行本规定进行协商，如不能达成一致意见，任何一方有权以书面通知方式提前终止本规定。</p>
               <p class="pad10_10 fontwB">第六十四条 特殊免责</p>
               <p class="pad10_10">（一）如因“爱心购”无法控制的原因使“爱心购”网上店铺系统崩溃或无法正常使用导致网上交易无法完成或丢失有关的信息、记录等，“爱心购”无需向商户承担任何责任。</p>
               <p class="pad10_10">（二）为保障网站的正常运行，“爱心购”需要定期或不定期地对网站进行停机维护；同时，基于市场整体利益考虑及经营需要，“爱心购”可能不定期对“爱心购”网上店铺栏目的服务内容、版面布局、页面设计等方面进行调整；如因上述情况影响“爱心购”在本规定项下义务的履行，商户同意予以谅解。“爱心购”则应尽可能将因此造成的不利影响减少到最低程度。</p>
               <p class="pad10_10 fontwB">第六十五条 争议解决</p>
               <p class="pad10_10">（一）在双方就本规定项下条款的解释和履行发生争议时，双方应以善意通过协商解决该争议。</p>
               <p class="pad10_10">（二）如协商不成，任何一方均可向“爱心购”所在地人民法院提起诉讼解决。</p>
               <p class="pad10_10">（三）本规定的订立、效力、执行和解释及争议的解决均应适用中国法律。</p>
               <p class="pad10_10 fontwB">第六十六条 恶意竞争</p>
               <p class="pad10_10">（一）定义和认定：</p>
               <p class="pad10_10">1、仅以低价格展示商品，却对买家的订单采取无货处理；或低价展示的商品无关配件无法使用的恶意行为；</p>
               <p class="pad10_10">2、商户恶意侵占其他商户库存，恶意差评等“爱心购”认定的恶意行为；</p>
               <p class="pad10_10">（二）违约：</p>
               <p class="pad10_10">商家违背以上承诺的，每次扣6分。</p>
               <p class="pad10_10 fontwB">第六十七条 其他规定</p>
               <p class="pad10_10">（一）如果本规定中的任何条款因违反法律或政府规定或因其他原因而完全或部分无效，则该条款被视为删除。但该条款的删除不影响本规定其他条款的效力。</p>
               <p class="pad10_10">（二）在法律允许的范围内，“爱心购”对本协议条款享有最终解释权。</p>
