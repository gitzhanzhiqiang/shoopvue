                    <p class="pad10_10">（2）商家对于本平台商家名和密码应严格保密并不得泄露给任何无关的第三方，如因商家保管不善而造成任何损失，商家应承担全部责任。</p>
                    <p class="pad10_10">2、税款缴纳</p>
                    <p class="pad10_10">双方应各自按照有关法律法规和国家政策的规定向有关的税务机关纳税。</p>
                    <p class="pad10_10">3、保密条款</p>
                    <p class="pad10_10">（1）任何一方对于因签署或履行本规定而了解或接触到的对方的机密资料和信息（以下简称"保密信息"）均应保密；非经对方书面同意，任何一方不得向第三方泄露、给予或转让该等保密信息。</p>
                    <p class="pad10_10">（2） 如对方提出要求，任何一方均应将载有对方保密信息的任何载体，按对方要求归还对方，或予以销毁，或进行其他处置，并且不得继续使用这些保密信息。</p>
                    <p class="pad10_10">（3）在本规定终止之后，各方在本规定项下的义务并不随之终止，各方仍需遵守本规定之保密条款，履行其所承诺的保密义务，直到其他方同意其解除此义务。</p>
                    <p class="pad10_10">（4）违约</p>
                    <p class="pad10_10">商家违背以上承诺的，每次扣36分。</p>
                    <p class="pad10_10">4、利益冲突</p>
                    <p class="pad10_10">（1）定义：</p>
                    <p class="pad10_10">A、在本规定有效期内，如有任何本平台或本平台关联企业在职员工成为商家的股东或者高级管理人员，商家必须立即书面通知本平台，如商家故意隐瞒此情况，本平台有权随时提前终止商家的业务而无需承担任何责任，由此造成的全部损失由商家自行承担。本平台无需退还商家已向本平台交纳的所有费用。</p>
                    <p class="pad10_10">B、商家不得为牟取不正当利益而对任何本平台或本平台关联企业在职员工有任何贿赂以及恶意投诉恶意诋毁本平台行象的行为，否则本平台有权随时单方提前终止本规定而无需承担任何责任，同时，商家应赔偿本平台因此遭受的全部损失。</p>
                    <p class="pad10_10">（2）违约：</p>
                    <p class="pad10_10">商家违背以上承诺的，每次扣36分。</p>
                    <p class="pad10_10">5、不可抗力</p>
                    <p class="pad10_10">（1）"不可抗力"是指本规定双方不能合理控制、不可预见或即使预见亦无法避免的事件，该事件妨碍、影响或延误任何一方根据本规定履行其全部或部分义务。该事件包括但不限于政府行为、自然灾害、战争、黑客袭击或任何其它类似事件等。</p>
                    <p class="pad10_10">（2）遭受不可抗力事件的一方可暂时中止履行本规定项下的义务直至不可抗力事件的影响消除为止，并且无需为此而承担违约责任。但应尽最大努力克服该事件，减轻其负面影响。</p>
                    <p class="pad10_10">（3）如不可抗力事件影响任何一方履行本规定项下义务的时间持续超过20天或累计超过30天，双方应就是否继续履行本规定进行协商，如不能达成一致意见，任何一方有权以书面通知方式提前终止本规定。</p>
                    <p class="pad10_10">6、特殊免责</p>
                    <p class="pad10_10">（1）如因本平台无法控制的原因使本平台网上店铺系统崩溃或无法正常使用导致网上交易无法完成或丢失有关的信息、记录等，本平台无需向商家承担任何责任。</p>
                    <p class="pad10_10">（2）为保障网站的正常运行，本平台需要定期或不定期地对网站进行停机维护；同时，基于市场整体利益考虑及经营需要，本平台可能不定期对本平台网上店铺栏目的服务内容、版面布局、页面设计等方面进行调整；如因上述情况影响本平台在本规定项下义务的履行，商家同意予以谅解。本平台则应尽可能将因此造成的不利影响减少到最低程度。</p>
                    <p class="pad10_10">7、争议解决</p>
                    <p class="pad10_10">（1）在双方就本规定项下条款的解释和履行发生争议时，双方应以善意通过协商解决该争议。</p>
                    <p class="pad10_10">（2）如协商不成，任何一方均可向本平台所在地人民法院提起诉讼解决。</p>
                    <p class="pad10_10">（3）本规定的订立、效力、执行和解释及争议的解决均应适用中国法律。</p>
                    <p class="pad10_10">8、恶意竞争</p>
                    <p class="pad10_10">（1）定义和认定：</p>
                    <p class="pad10_10">A、仅以低价格展示商品，却对买家的订单采取无货处理；或低价展示的商品无关配件无法使用的恶意行为；</p>
                    <p class="pad10_10">B、商家恶意侵占其他商家库存，恶意差评等本平台认定的恶意行为；</p>
                    <p class="pad10_10">（2）违约：</p>
                    <p class="pad10_10">商家违背以上承诺的，每次扣6分。</p>
                    <p class="pad10_10">9、其他规定</p>
                    <p class="pad10_10">（1）如果本规定中的任何条款因违反法律或政府规定或因其他原因而完全或部分无效，则该条款被视为删除。但该条款的删除不影响本规定其他条款的效力。</p>
                    <p class="pad10_10">（2）在法律允许的范围内，本平台对本协议条款享有最终解释权。</p>
                    <p class="pad10_10">（3）本管理规定与《本平台合作协议》即主合同具有同等法律效力。</p>

