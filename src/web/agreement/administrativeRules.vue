                    <p class="pad10_10">G、商家拒绝按照承诺给“本平台”、买家提供服务的;</p>
                    <p class="pad10_10">H、商家未履行其它承诺的;</p>
                    <p class="pad10_10">（2）违约：</p>
                    <p class="pad10_10">A、公告警示;</p>
                    <p class="pad10_10">B、商家违背以上承诺的，每次扣3分;</p>
                    <p class="pad10_10">2、线下交易</p>
                    <p class="pad10_10">（1）定义：</p>
                    <p class="pad10_10">线下交易是指通过本平台购物平台下订单，不通过本平台支付系统付款的订单，即由买家直接联系商家进行双方协商购买的交易行为。</p>
                    <p class="pad10_10">（2）认定：</p>
                    <p class="pad10_10">本平台对通过本平台购物平台产生的订单实时监控，根据订单实际发生与否确定线下交易行为是否成立。</p>
                    <p class="pad10_10">（3）违约：</p>
                    <p class="pad10_10">商家违背以上承诺的，每次扣18分（签保底协议的除外）;</p>
                    <p class="pad10_10">赔偿：商家需向本平台缴纳5倍的线下交易订单佣金，作为赔偿;</p>
                    <p class="pad10_10">3、虚假宣传</p>
                    <p class="pad10_10">是指商业活动中商家利用广告或其他宣传方法对商品或者服务发布与实际内容不相符的虚假信息，导致买家产生误解的行为；虚假宣传的，本平台将对商家采取每次扣12分的处罚，对涉及虚假宣传的商品或信息进行删除，同时因虚假宣传违规损害买家体验的，商家需按退一赔三最低500元标准对买家予以赔付；</p>
                    <p class="pad10_10">4、恶意骚扰买家</p>
                    <p class="pad10_10">指商家交易中或交易后采取恶劣手段骚扰买家，妨害他人合法权益的行为,每次扣12分。</p>
                    <p class="pad10_10">5、泄露他人信息</p>
                    <p class="pad10_10">是指未经允许发布、传递他人隐私信息，涉嫌侵犯他人隐私权的行为。泄露他人信息的，每次扣6分。</p>
