                    <p class="pad10_10">指商家盗用他人本平台账户，涉嫌侵犯他人相关信息、财产权的行为,每次扣48分。</p>
                    <p class="pad10_10">7、骗取他人财物</p>
                    <p class="pad10_10">是指商家以非法获利为目的，非法获取他人财物，涉嫌侵犯他人财产权的行为,每次扣48分。</p>
                    <p class="pad10_10 fontwB">十一、服务管理规定</p>
                    <p class="pad10_10">1、商家服务政策及规范</p>
                    <p class="pad10_10">（1）定义和认定：</p>
                    <p class="pad10_10">入驻商家需按下列规定执行。</p>
                    <p class="pad10_10">（2）违约：</p>
                    <p class="pad10_10">A、若商家服务态度恶劣、使用不文明语言，罚款50元~500元，关闭店铺7天，影响恶劣的追加500--2000元的罚款；</p>
                    <p class="pad10_10">B、退换货退款售后类问题，如商家未按规定时间回复处理意见，则视为商家对客户提出的售后服务无异议，本平台有权按照客户要求为其回复处理意见，商家应按照本平台的处理意见为客户提供售后服务，因此而可能产生的损失或额外费用将由商家自行承担；</p>
                    <p class="pad10_10">C、对于被媒体发布从而对本平台产生不利社会影响的客户投诉或被工商局等相关部门受理的重大客户投诉，商家未按处理时效处理，本平台有权要求卖家承担人民币10000元以上的违约金，并要求卖家立即下架相应的商品，停止销售，进行整改，直至终止与卖家的合作，如因此给本平台造成损失的，卖家应承担全部责任；</p>
                    <p class="pad10_10">D、对于通过论坛、微博等途径传播对本平台产生不利社会影响的客户投诉或被工商局等相关部门受理的一般客户投诉，商家未按处理时效处理，本平台有权要求卖家承担人民币5000元以上的违约金，并要求卖家立即下架相应的商品,如因此给本平台造成损失的，卖家应承担全部责任。</p>
                    <p class="pad10_10">E、问题响应时效 ：买家的诉求，本平台会通知（通知形式包括但不限于工单、电话、邮件、“本平台”在线客服）商家处理，商家需按以下时效进行处理。</p>
                    <p class="pad10_10">咨询类+查询类问题，商家需在接到客服反馈后3小时内，给出处理方案或者处理时效，处理时效应在24小时（以“本平台”通知时间计算自然小时）内。</p>
                    <p class="pad10_10">退换货退款售后类问题，商家需在接到客服反馈后3小时内，给出处理方案或者处理时效，处理时效应在24小时（以“本平台”通知时间计算自然小时）内。</p>
                    <p class="pad10_10">媒体、工商、消协投诉的，商家需在接到客服反馈后1小时内，给出处理方案或者处理时效，处理时效应在24小时（以“本平台”通知时间计算自然小时）内。</p>
                    <p class="pad10_10">论坛、微博等公共平台，商家需在接到客服反馈后1小时内，给出处理方案或者处理时效，处理时效应在24小时（以“本平台”通知时间计算自然小时）内。</p>
                    <p class="pad10_10">F、违反本规定任何一项，处罚每次扣1分。</p>
                    <p class="pad10_10">2、不配合平台管理</p>
                    <p class="pad10_10">（1）是指商家在日常运营中不配合平台管理的行为。每次扣2分。包括但不限于骚扰、谩骂、恐吓平台工作人员，散播恶意攻击平台的消息等情形；对于不配合平台管理的商家“本平台”将采取警告公示、商品下架、限制发布商品等违规处理措施。</p>
                    <p class="pad10_10">（2）本平台有权对发货率、投诉率等指标达成较差商家进行约谈，需商家给出书面分析及改进措施。持续未改进将扣除0.5分，情节严重追加扣除0.5分。</p>
